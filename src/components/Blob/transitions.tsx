export const bodyTransition = (x: number, y: number) =>
  `translate3d(${x / 10}px,${y / 10}px,0)`;
export const eyesTransition = (x: number, y: number) =>
  `translate3d(${x / 20}px,${y / 20}px,0)`;
export const mouthTransition = (x: number, y: number) =>
  `translate3d(${x / 22}px,${y / 22}px,0)`;
